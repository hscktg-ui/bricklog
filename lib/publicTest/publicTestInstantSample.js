const INSTANT_PAD_SUFFIXES = [
  (s) => `${s.region}에서 ${s.brandName} 예약·문의 시간은 공식 채널에 표시됩니다.`,
  (s) => `${s.brandName} 방문 전 준비물·인원·일정을 미리 알려 주시면 상담이 빨라집니다.`,
  (s) => `처음 오시는 분은 ${s.region} 위치·주차 안내를 함께 받으실 수 있습니다.`,
  (s) => `${s.brandName}은 확인 가능한 정보만 안내하며, 글에 없는 할인은 적지 않습니다.`,
  (s) => `문의는 영업시간 내 전화·메시지가 가장 빠르며, 혼잡 시간대는 미리 예약을 권합니다.`,
  (s) => `${s.region} ${s.brandName} 공식 안내 기준으로 방문 일정을 조율해 드립니다.`,
];

function padInstantBody(body, sample, index = 0) {
  const trimmed = String(body || "").trim();
  if (trimmed.length >= 80) return trimmed;
  const extra = INSTANT_PAD_SUFFIXES[index % INSTANT_PAD_SUFFIXES.length](sample);
  return `${trimmed} ${extra}`.trim();
}

const PAD_SUFFIXES = [
  (s) => `${s.brandName} ${s.region} 기준 안내입니다.`,
  (s) => `${s.brandName} 공식 채널에서 최신 정보를 확인할 수 있습니다.`,
  (s) => `방문·예약 전 ${s.brandName} 영업 안내를 함께 확인해 보세요.`,
  (s) => `${s.region} 인근에서 ${s.brandName}을 찾는 분들에게 도움이 됩니다.`,
];

function padProseBody(body, sample, min = 100, slot = 0) {
  let text = String(body || "").trim();
  if (text.length >= min) return text;
  const extra = PAD_SUFFIXES[slot % PAD_SUFFIXES.length](sample);
  return `${text} ${extra}`.trim();
}

